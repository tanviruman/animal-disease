async def _fetch_hf_embedding(text: str) -> Optional[np.ndarray]:
    """
    Calls the HF free inference API to get a 384-dim embedding for `text`.
    Returns None if the token is missing, the API is unavailable, or any
    error occurs — allowing graceful fallback to layers 1+2 only.
    """
    if not _HF_TOKEN:
        return None
    try:
        async with httpx.AsyncClient(timeout=15.0) as client:
            resp = await client.post(
                _HF_URL,
                headers=_HF_HEADERS,
                json={"inputs": text},
            )
        if resp.status_code != 200:
            return None
        data = resp.json()
        # API can return [vec] or [[vec]] — normalise both
        if isinstance(data[0], list):
            vec = data[0]
        else:
            vec = data
        return np.array(vec, dtype=np.float32).reshape(1, -1)
    except Exception:
        return None


async def _extract_symptoms_logic(req: TextRequest):
    """
    Three-layer symptom extraction pipeline:

    Layer 1 — Direct substring match (zero latency, no API)
        Catches obvious exact or near-exact mentions in the text.

    Layer 2 — Synonym regex rules (zero latency, no API)
        Catches natural synonyms: 'limping' → Lameness, 'won't eat' → Appetite Loss.

    Layer 3 — HF semantic embedding + cosine similarity (async, only when needed)
        Embeds the user's full sentence with all-MiniLM-L6-v2 and compares
        against pre-calculated phrase vectors for every canonical symptom.
        Only invoked when layers 1+2 found fewer than 2 symptoms.
        Gracefully skipped if HF_TOKEN is not set or the API is unavailable.
    """
    if not req.text.strip():
        return {"matched_symptoms": []}

    user_query = req.text.lower().strip()
    detected: list[str] = []

    # ── Layer 1: Direct substring check ──────────────────────────────────────
    for sym in master_symptoms:
        if sym.lower() in user_query and sym not in detected:
            detected.append(sym)

    # ── Layer 2: Synonym / regex rules ───────────────────────────────────────
    for sym, keywords in SYNONYM_RULES.items():
        if sym in _master_sym_set and sym not in detected:
            for kw in keywords:
                if re.search(r'\b' + re.escape(kw) + r'\b', user_query):
                    detected.append(sym)
                    break

    # ── Layer 3: Semantic embedding fallback (HF API) ─────────────────────────
    # Only call the HF API when the fast layers didn't catch enough symptoms.
    # This keeps latency low for simple, obvious inputs.
    if len(detected) < 2:
        user_vec = await _fetch_hf_embedding(user_query)
        if user_vec is not None:
            sims = cosine_similarity(user_vec, _sym_embeddings)[0]  # shape (N,)

            # Walk from highest similarity downward — keep track of (label, score)
            top_indices = np.argsort(sims)[::-1]
            sem_candidates: list[tuple[str, float]] = []
            for idx in top_indices:
                if sims[idx] < _SEM_THRESHOLD:
                    break
                candidate = _sym_labels[idx]
                if candidate in _master_sym_set and candidate not in detected:
                    if not any(x[0] == candidate for x in sem_candidates):
                        sem_candidates.append((candidate, float(sims[idx])))

            # Apply mutual exclusion: Appetite Loss vs Increased Appetite
            if "Appetite Loss" in detected:
                sem_candidates = [x for x in sem_candidates if x[0] != "Increased Appetite"]
            if "Increased Appetite" in detected:
                sem_candidates = [x for x in sem_candidates if x[0] != "Appetite Loss"]

            app_loss_score = next((x[1] for x in sem_candidates if x[0] == "Appetite Loss"), None)
            inc_app_score  = next((x[1] for x in sem_candidates if x[0] == "Increased Appetite"), None)
            if app_loss_score is not None and inc_app_score is not None:
                if app_loss_score >= inc_app_score:
                    sem_candidates = [x for x in sem_candidates if x[0] != "Increased Appetite"]
                else:
                    sem_candidates = [x for x in sem_candidates if x[0] != "Appetite Loss"]

            # Add up to 8 unique symptoms
            for candidate, score in sem_candidates:
                detected.append(candidate)
                if len(detected) >= 8:
                    break

    return {"matched_symptoms": detected}


