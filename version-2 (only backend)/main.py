# ── Helper functions ──────────────────────────────────────────────────────────

def get_urgency(disease_name: str) -> str:
    name_lower = disease_name.lower()
    if any(c in name_lower for c in ["parvovirus", "rabies", "bloat", "anthrax", "foot and mouth", "african swine", "haemorrhagic", "septicaemia", "fever (haemorrhagic)"]):
        return "Emergency"
    if any(m in name_lower for m in ["healthy", "pregnancy"]):
        return "Monitor"
    return "Schedule Vet Visit"


def lookup_disease_info(name: str) -> dict:
    """Multi-layer disease info lookup: direct match -> alias map -> inline info -> fallback."""
    # Layer 1: direct key match in disease_info.json
    if name in disease_info:
        info = disease_info[name]
        if info.get("description", "") not in ("Clinical medical condition.", ""):
            return info

    # Layer 2: alias map -> disease_info.json
    alias = DISEASE_ALIAS.get(name)
    if alias and alias in disease_info:
        info = disease_info[alias]
        if info.get("description", "") not in ("Clinical medical condition.", ""):
            return info

    # Layer 3: inline curated info
    if name in INLINE_INFO:
        return INLINE_INFO[name]

    # Layer 4: fallback
    urg = get_urgency(name)
    return {
        "description": f"{name} is a recognized veterinary condition requiring professional evaluation.",
        "treatment": "Seek emergency veterinary care immediately. Do not delay." if urg == "Emergency"
                     else "Consult a veterinarian for a full diagnosis and tailored treatment plan.",
        "prevention": "Maintain regular veterinary check-ups, follow vaccination schedules, and practice good animal husbandry."
    }


def validate_vitals(animal_type: str, temperature: Optional[float], heart_rate: Optional[float]) -> Optional[str]:
    """
    Returns an error message string if vitals are unrealistically out of range,
    or None if they are acceptable.
    """
    if temperature is not None:
        if temperature < TEMP_ABSOLUTE_MIN or temperature > TEMP_ABSOLUTE_MAX:
            return (
                f"Temperature {temperature}°C is outside the biologically possible range "
                f"({TEMP_ABSOLUTE_MIN}–{TEMP_ABSOLUTE_MAX}°C). Please re-check and enter a valid reading."
            )
        normal = VITALS_RANGES.get(animal_type, {}).get("temp")
        if normal:
            lo, hi = normal
            # Allow 3°C outside normal range before flagging as unrealistic
            if temperature < (lo - 3) or temperature > (hi + 3):
                return (
                    f"Temperature {temperature}°C is far outside the normal range for {animal_type} "
                    f"({lo}–{hi}°C). Please verify your reading — this value appears unrealistic."
                )

    if heart_rate is not None:
        if heart_rate < HR_ABSOLUTE_MIN or heart_rate > HR_ABSOLUTE_MAX:
            return (
                f"Heart rate {heart_rate} bpm is outside the biologically possible range "
                f"({HR_ABSOLUTE_MIN}–{HR_ABSOLUTE_MAX} bpm). Please re-check your measurement."
            )
        normal = VITALS_RANGES.get(animal_type, {}).get("hr")
        if normal:
            lo, hi = normal
            # Allow 3x outside normal range before flagging as unrealistic
            margin_lo = max(lo - 40, HR_ABSOLUTE_MIN)
            margin_hi = hi + 100
            if heart_rate < margin_lo or heart_rate > margin_hi:
                return (
                    f"Heart rate {int(heart_rate)} bpm is far outside the normal range for {animal_type} "
                    f"({lo}–{hi} bpm). Please verify your reading — this value appears unrealistic."
                )
    return None


